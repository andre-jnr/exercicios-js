/**
 * Escreva um programa que leia um valor em metros e
 * o exiba convertido em centímetros e milímetros.
 */
var metros = Number(prompt('Digite um valor'))
var centimetros = metros * 100
var milimetros = centimetros * 100

console.log(`Centímetros: ${centimetros}cm`)
console.log(`Milímetros: ${milimetros}mm`)
